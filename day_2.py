# Subscripting - pulling out a particular element from a string
print("Hello"[4])

# String
print("123" + "456") # concatenation

# Integer = Whole numbers
print(123 + 456)

# Large Integers - ie., when we have large numbers (123,456,00) we separate the commas with underscores
print(123_456_789)

# Float - Floating Point Number
print(3.14159)

# Boolean - True / False
print(True)
print(False)
